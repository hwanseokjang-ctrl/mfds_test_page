from __future__ import annotations

from dataclasses import dataclass


def render(template: str, **kwargs: str) -> str:
    """``{name}`` 자리표시자를 사전에 따라 치환해 반환."""

    out = template
    for name, value in kwargs.items():
        out = out.replace("{" + name + "}", str(value))
    return out


